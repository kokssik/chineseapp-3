    <h1>ČÍNŠTINA</h1>
    <div>
      Čínština není jen jeden jazyk. Je to stovka jazyků, které jsou vzájemně
      nesrozumitelné, mají však jedno společného - písmo. Všechny čínské jazyky
      se píší stejným písmem, věty tedy mohou znít v každé části Číny úplně
      jinak, ale po napsání jsou se mluvčí různých variant čínštiny mezi sebou
      domluvit.
    </div>
    <div><h1>O APLIKACI</h1></div>
    <div>
      V této aplikaci se můžete naučit základy Mandarínské čínštiny -
