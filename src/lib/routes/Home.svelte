      kurikula zkoušky HSK 1.
    </div>
    <div><h1>ČÍNSKÁ VÝSLOVNOST</h1></div>
    <div>
      Přepis čínštiny do latinky má své zvláštnosti, pokud je však známe,
      nebudeme mít s výslovností problém.
    </div>
    <div><h2>TÓN</h2></div>
    <div>
      Nejsložitější na výslovnosti čínštiny je tón. Ty se píší buď jako
      diakritická znaménka, nebo číslo za slabikou. Tóny má čínština 4 - vysoký
      (1), rostoucí (2), klesající-rostoucí (3) a klesající (4).
    </div>
